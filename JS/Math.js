// 1 --> negative to positive and positive to positive only 
console.log(Math.abs(10));       // 10
console.log(Math.abs(-10));      // 10

// 2 --> increase the value by 1 
console.log(Math.ceil(20.3));    // 21

// 3 --> floor (remove decimal)
console.log(Math.floor(10.779)); // 10

// 4 --> round to nearest integer
console.log(Math.round(13.5));   // 14
console.log(Math.round(12.2));   // 12

// 5 --> truncate decimals
console.log(Math.trunc(47.888666)); // 47

// 6 --> power
console.log(Math.pow(10, 3));    // 1000

// 7 --> maximum value
console.log(Math.max(17, 12, 36, 99)); // 99

// 8 --> minimum value
console.log(Math.min(1, 4, 2, 6, 33));  // 1

